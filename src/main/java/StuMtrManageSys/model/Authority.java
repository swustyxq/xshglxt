package StuMtrManageSys.model;

public class Authority {
    private Integer authId;

    private String authName;

    private String authUsertype;

    private String authUrl;

    private Integer authRank;

    public Integer getAuthId() {
        return authId;
    }

    public void setAuthId(Integer authId) {
        this.authId = authId;
    }

    public String getAuthName() {
        return authName;
    }

    public void setAuthName(String authName) {
        this.authName = authName;
    }

    public String getAuthUsertype() {
        return authUsertype;
    }

    public void setAuthUsertype(String authUsertype) {
        this.authUsertype = authUsertype;
    }

    public String getAuthUrl() {
        return authUrl;
    }

    public void setAuthUrl(String authUrl) {
        this.authUrl = authUrl;
    }

    public Integer getAuthRank() {
        return authRank;
    }

    public void setAuthRank(Integer authRank) {
        this.authRank = authRank;
    }
}